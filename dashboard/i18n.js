// linear interp between keyframes
window.SaamidSampleTrack = function(t) {
  const path = window.SaamidFlightPath;
  if (t <= 0) return { ...path[0] };
  if (t >= 1) return { ...path[path.length - 1] };
  for (let i = 0; i < path.length - 1; i++) {
    const a = path[i], b = path[i + 1];
    if (t >= a.t && t <= b.t) {
      const u = (t - a.t) / (b.t - a.t);
      // slope across the segment, in metres per sim-second (24s loop)
      const segDur = (b.t - a.t) * 24;
      const vx = (b.x - a.x) / segDur;
      const vy = (b.y - a.y) / segDur;
      const speed = Math.sqrt(vx*vx + vy*vy);
      return {
        t,
        x: a.x + (b.x - a.x) * u,
        y: a.y + (b.y - a.y) * u,
        alt: a.alt + (b.alt - a.alt) * u,
        prob: a.prob + (b.prob - a.prob) * u,
        type: u < 0.5 ? a.type : b.type,
        speed, vx, vy,
      };
    }
  }
  return { ...path[0] };
};
